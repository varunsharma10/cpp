#define getunique(v) {sort(v.begin(), v.end()); v.erase(unique(v.begin(), v.end()), v.end());}
void solve()
{    
    ll n;
    cin>>n;
    ll ans=0;
    for (int i = 1; i <= n / 2; ++i) {
		ans += i * 1ll * i;
		}
    cout<<ans*8<<endl;       
}
